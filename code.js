
//Item details pop-up
$(document).ready(function(){
  $('.box').hover(function() {
    var showDetails = '#details-' + this.id;
    $(showDetails).show(100);
    console.log('hoved');
  }, function() {
    var showDetails = '#details-' + this.id;
    $(showDetails).hide(100);
    console.log('unhoved');
  });
});

//Slider layout
function goToSection (num) {
  var sectionName = 'xpos' + num;
  $('body').removeClass().addClass(sectionName);
}


